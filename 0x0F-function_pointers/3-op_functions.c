/**
 * op_add - adds two numbers
 * @a: first num
 * @b: second num
 *
 * Return: returns sum of two nums
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_add - subtracts two numbers
 * @a: first num
 * @b: second num
 *
 * Return: returns sub of two nums
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_add - Multiplies two numbers
 * @a: first num
 * @b: second num
 *
 * Return: returns mul of two nums
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_add - divides two numbers
 * @a: first num
 * @b: second num
 *
 * Return: returns div of two nums
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_add - mods two numbers
 * @a: first num
 * @b: second num
 *
 * Return: returns mod of two nums
 */
int op_mod(int a, int b)
{
	return (a % b);
}
